# =============================================================================
#  ENTRYPOINT
# =============================================================================
main() {
    local cmd="${1:-help}"
    case "${cmd}" in
        generate) cmd_generate ;;
        docker)   cmd_docker   ;;
        compose)  cmd_compose  ;;
        k8s)      cmd_k8s      ;;
        systemd)  cmd_systemd  ;;
        health)   cmd_health   ;;
        logs)     cmd_logs     ;;
        stop)     cmd_stop     ;;
        help|--help|-h) cmd_help ;;
        *)
            err "Unknown command: ${cmd}"
            cmd_help
            exit 1
            ;;
    esac
}

main "$@"
