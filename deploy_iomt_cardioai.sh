#!/usr/bin/env bash
# =============================================================================
#  IoMT CardioAI — Single-File Deployment Script
#  Version  : 1.0.0
#  Requires : bash 4+, docker (optional), kubectl (optional)
# =============================================================================
#
#  USAGE
#  -----
#    chmod +x deploy_iomt_cardioai.sh
#    ./deploy_iomt_cardioai.sh [COMMAND]
#
#  COMMANDS
#  --------
#    generate      Write all deployment artefacts to ./deploy/
#    docker        Build Docker image and run container
#    compose       Start full stack with docker-compose
#    k8s           Apply Kubernetes manifests (requires kubectl)
#    systemd       Install and start systemd service (requires sudo)
#    health        Hit the /health endpoint and print bridge status
#    logs          Tail logs for the running container / service
#    stop          Gracefully stop whichever runtime is active
#    help          Show this message
#
#  ENVIRONMENT VARIABLES  (required before running)
#  --------------------------------------------------
#    IOMT_SERVER_WS_URL      wss://iomt.hospital.local/stream
#    CARDIOAI_BACKEND_ID     cardioai-prod-01
#    IOMT_SHARED_SECRET      <32+ char secret — from Vault / SSM>
#    IOMT_JWT_SECRET         <32+ char secret — from Vault / SSM>
#
#  All other variables are optional — see .env.example generated by
#  the `generate` command for the full list with defaults.
#
# =============================================================================

