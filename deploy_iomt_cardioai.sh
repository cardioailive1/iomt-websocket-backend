  ${GREEN}compose${NC}    Start full stack with docker-compose
  ${GREEN}k8s${NC}        Apply Kubernetes manifests (kubectl required)
  ${GREEN}systemd${NC}    Install systemd service (sudo required)
  ${GREEN}health${NC}     Probe /health endpoint and print bridge status
  ${GREEN}logs${NC}       Tail logs (container or systemd)
  ${GREEN}stop${NC}       Gracefully stop any running instance
  ${GREEN}help${NC}       Show this message

${BOLD}REQUIRED ENVIRONMENT VARIABLES${NC}
