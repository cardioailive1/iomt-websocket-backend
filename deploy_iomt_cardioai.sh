# =============================================================================
#  COMMAND: docker — build and run a single container
# =============================================================================
cmd_docker() {
    hdr "Docker Build & Run"

    _check_secrets
    _assert_cmd docker

    info "Building image ${IMAGE_NAME}:${IMAGE_TAG} ..."
    docker build -t "${IMAGE_NAME}:${IMAGE_TAG}" "${DEPLOY_DIR}"
    log "Image built: ${IMAGE_NAME}:${IMAGE_TAG}"

    # Stop existing container if running
    docker stop "${CONTAINER_NAME}" 2>/dev/null || true
    docker rm   "${CONTAINER_NAME}" 2>/dev/null || true

    info "Starting container ${CONTAINER_NAME} ..."
    docker run -d \
        --name "${CONTAINER_NAME}" \
        --restart unless-stopped \
        -e "IOMT_SERVER_WS_URL=${IOMT_SERVER_WS_URL}" \
        -e "CARDIOAI_BACKEND_ID=${CARDIOAI_BACKEND_ID}" \
        -e "IOMT_SHARED_SECRET=${IOMT_SHARED_SECRET}" \
        -e "IOMT_JWT_SECRET=${IOMT_JWT_SECRET}" \
        -e "LOG_FORMAT=json" \
        -e "LOG_LEVEL=${LOG_LEVEL:-INFO}" \
        -p "${WS_PORT}:8765" \
        -p "${HEALTH_PORT}:8080" \
        "${IMAGE_NAME}:${IMAGE_TAG}"

    log "Container started: ${CONTAINER_NAME}"
    info "WebSocket: ws://localhost:${WS_PORT}"
    info "Health:    http://localhost:${HEALTH_PORT}/health"
    info "Logs:      docker logs -f ${CONTAINER_NAME}"
}

# =============================================================================
#  COMMAND: compose — start full stack with docker-compose
# =============================================================================
cmd_compose() {
    hdr "Docker Compose Up"

    _check_secrets
    _assert_cmd docker

    if [[ ! -f "${DEPLOY_DIR}/.env" ]]; then
        err ".env not found. Run: cp ${DEPLOY_DIR}/.env.example ${DEPLOY_DIR}/.env"
        err "Then fill in your secrets before running compose."
        exit 1
    fi

    info "Starting stack with docker-compose ..."
    (cd "${DEPLOY_DIR}" && docker compose up -d --build)
    log "Stack started"
    info "Health: http://localhost:${HEALTH_PORT}/health"
}

# =============================================================================
#  COMMAND: k8s — apply Kubernetes manifests
# =============================================================================
cmd_k8s() {
    hdr "Kubernetes Deployment"

    _assert_cmd kubectl

    # Verify secret placeholder has been replaced
    if grep -q "REPLACE_VIA_VAULT" "${DEPLOY_DIR}/k8s/secret.yaml" 2>/dev/null; then
        warn "secret.yaml still contains placeholder values."
        warn "For production, populate secrets via:"
        warn "  kubectl create secret generic cardioai-secrets \\"
        warn "    --namespace=cardioai \\"
        warn "    --from-literal=shared_secret=\"\$(vault kv get -field=shared_secret secret/cardioai)\" \\"
        warn "    --from-literal=jwt_secret=\"\$(vault kv get -field=jwt_secret secret/cardioai)\""
        echo ""
        read -r -p "Continue anyway? (not recommended for production) [y/N] " yn
        [[ "${yn}" =~ ^[Yy]$ ]] || { info "Aborted."; exit 0; }
    fi

    info "Applying manifests from ${DEPLOY_DIR}/k8s/ ..."
    kubectl apply -k "${DEPLOY_DIR}/k8s/"

    info "Waiting for rollout ..."
    kubectl rollout status deployment/cardioai -n cardioai --timeout=120s

    log "Deployment complete"
    kubectl get pods -n cardioai -o wide
}

# =============================================================================
#  COMMAND: systemd — install and enable systemd service
# =============================================================================
cmd_systemd() {
    hdr "Systemd Service Installation"

    if [[ $EUID -ne 0 ]]; then
        err "systemd installation requires root. Re-run with sudo."
        exit 1
    fi

    _check_secrets

    # Create user
    if ! id "${SERVICE_USER}" &>/dev/null; then
        useradd --system --no-create-home --shell /bin/false "${SERVICE_USER}"
        log "Created system user: ${SERVICE_USER}"
    fi

    # Create app directory
    mkdir -p "${APP_DIR}/logs"
    chown -R "${SERVICE_USER}:${SERVICE_USER}" "${APP_DIR}"

    # Copy application files (assumes they are in CWD)
    for f in iomt_cardioai_production.py IoMT_implementation.py \
              IoMT_clinical_workflow.py IoMT_gcp_compduide.py; do
        if [[ -f "./${f}" ]]; then
            cp "./${f}" "${APP_DIR}/"
            log "Copied ${f} → ${APP_DIR}/"
        else
            warn "${f} not found in current directory — copy manually to ${APP_DIR}/"
        fi
    done

    # Write .env
    if [[ ! -f "${APP_DIR}/.env" ]]; then
        cat > "${APP_DIR}/.env" << ENVFILE
IOMT_SERVER_WS_URL=${IOMT_SERVER_WS_URL:-wss://iomt.hospital.local/stream}
CARDIOAI_BACKEND_ID=${CARDIOAI_BACKEND_ID:-cardioai-prod-01}
IOMT_SHARED_SECRET=${IOMT_SHARED_SECRET}
IOMT_JWT_SECRET=${IOMT_JWT_SECRET}
LOG_FORMAT=json
LOG_LEVEL=${LOG_LEVEL:-INFO}
ENVFILE
        chmod 600 "${APP_DIR}/.env"
        chown "${SERVICE_USER}:${SERVICE_USER}" "${APP_DIR}/.env"
        log "Written ${APP_DIR}/.env (mode 600)"
    else
        info "${APP_DIR}/.env already exists — not overwritten"
    fi

    # Install dependencies
    pip3 install --quiet websockets aiohttp pyjwt numpy
    log "Python dependencies installed"

    # Install service unit
    cp "${DEPLOY_DIR}/cardioai.service" /etc/systemd/system/cardioai.service
    systemctl daemon-reload
    systemctl enable cardioai
    systemctl restart cardioai
    systemctl status cardioai --no-pager

    log "Service installed and started"
    info "Logs: journalctl -u cardioai -f"
}

# =============================================================================
#  COMMAND: health — probe the /health endpoint
# =============================================================================
cmd_health() {
    hdr "Health Check"

    _assert_cmd curl

    URL="http://localhost:${HEALTH_PORT}/health"
    info "Probing ${URL} ..."

    RESPONSE=$(curl -sf --max-time 10 "${URL}") || {
        err "UNHEALTHY — no response from ${URL}"
        exit 1
    }

    echo "${RESPONSE}" | python3 -m json.tool 2>/dev/null || echo "${RESPONSE}"
    echo ""
    log "HEALTHY"
}

# =============================================================================
#  COMMAND: logs — tail logs
# =============================================================================
cmd_logs() {
    if docker ps --format '{{.Names}}' 2>/dev/null | grep -q "${CONTAINER_NAME}"; then
        docker logs -f "${CONTAINER_NAME}"
    elif systemctl is-active cardioai &>/dev/null; then
        journalctl -u cardioai -f
    else
        err "No running cardioai container or systemd service found."
        exit 1
    fi
}

# =============================================================================
#  COMMAND: stop — gracefully stop the service
# =============================================================================
cmd_stop() {
    hdr "Stopping IoMT CardioAI"

    # Docker
    if docker ps --format '{{.Names}}' 2>/dev/null | grep -q "${CONTAINER_NAME}"; then
        docker stop "${CONTAINER_NAME}"
        docker rm   "${CONTAINER_NAME}"
        log "Docker container stopped"
    fi

    # Compose
    if [[ -f "${DEPLOY_DIR}/docker-compose.yml" ]]; then
        (cd "${DEPLOY_DIR}" && docker compose down 2>/dev/null || true)
    fi

    # Systemd
    if systemctl is-active cardioai &>/dev/null 2>&1; then
        systemctl stop cardioai
        log "Systemd service stopped"
    fi

    log "Done"
}

# =============================================================================
#  HELPERS
# =============================================================================
_assert_cmd() {
    command -v "$1" &>/dev/null || {
        err "Required command not found: $1"
        exit 1
    }
}

_check_secrets() {
    local missing=0
    for var in IOMT_SERVER_WS_URL CARDIOAI_BACKEND_ID IOMT_SHARED_SECRET IOMT_JWT_SECRET; do
        if [[ -z "${!var:-}" ]]; then
            err "Required environment variable not set: ${var}"
            missing=1
        fi
    done
    if [[ "${missing}" -eq 1 ]]; then
        echo ""
        err "Set the missing variables and re-run."
        err "Example:"
        err "  export IOMT_SERVER_WS_URL='wss://iomt.hospital.local/stream'"
        err "  export CARDIOAI_BACKEND_ID='cardioai-prod-01'"
        err "  export IOMT_SHARED_SECRET=\"\$(vault kv get -field=shared_secret secret/cardioai)\""
        err "  export IOMT_JWT_SECRET=\"\$(vault kv get -field=jwt_secret secret/cardioai)\""
        exit 1
    fi
}

